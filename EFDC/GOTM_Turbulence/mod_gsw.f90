!==========================================================================
function gsw_pt_from_t (sa, t, ps, p_ref) 
!==========================================================================
!   
! Calculates potential temperature of seawater from in-situ temperature 
!
! sa     : Absolute Salinity                               [g/kg]
! t      : in-situ temperature                             [deg C]
! p      : sea pressure                                    [dbar]
! p_ref  : reference sea pressure                          [dbar]
!
! gsw_pt_from_t : potential temperature                    [deg C]
!--------------------------------------------------------------------------

implicit none

real (rkd), parameter :: gsw_cp0 = 3991.86795711963
real (rkd), parameter :: gsw_sso = 35.16504
real (rkd), parameter :: gsw_t0 = 273.15
real (rkd), parameter :: gsw_ups = gsw_sso/35.0

real (rkd), intent(in) :: sa, t, ps, p_ref 

real (rkd) :: gsw_pt_from_t

integer no_iter
real (rkd) :: s1, pt, pt_old, dentropy, dentropy_dt
real (rkd) :: true_entropy_part, ptm

integer, parameter :: n0 = 0, n2 = 2

s1 = sa/gsw_ups

pt = t + (ps-p_ref)*( 8.65483913395442e-6 - &
               s1 *  1.41636299744881e-6 - &
         (ps+p_ref)*  7.38286467135737e-9 + &
               t  *(-8.38241357039698e-6 + &
               s1 *  2.83933368585534e-8 + &
               t  *  1.77803965218656e-8 + &
         (ps+p_ref)*  1.71155619208233e-10))

dentropy_dt = gsw_cp0/((gsw_t0 + pt)*(1.0 - 0.05*(1.0 - sa/gsw_sso)))

true_entropy_part = gsw_entropy_part(sa,t,ps)

do no_iter = 1, 2
    pt_old = pt
    dentropy = gsw_entropy_part(sa,pt_old,p_ref) - true_entropy_part
    pt = pt_old - dentropy/dentropy_dt 
    ptm = 0.5*(pt + pt_old)
    dentropy_dt = -gsw_gibbs(n0,n2,n0,sa,ptm,p_ref)
    pt = pt_old - dentropy/dentropy_dt
end do

gsw_pt_from_t = pt

return
end function

