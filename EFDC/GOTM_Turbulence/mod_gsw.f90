function buoy_unesco (sa,temp,rho0) 
!==========================================================================
!   
! Calculates water density using unesco formula
!--------------------------------------------------------------------------

implicit none
real (rkd), intent(in) :: sa, temp, rho0
real (rkd) :: buoy_unesco
real (rkd) :: rho, rhtmp

rhtmp = 999.842594 + 6.793952D-2*temp - 9.095290D-3*temp*temp + 1.001685D-4*temp*temp*temp - 1.120083D-6*temp*temp*temp*temp + 6.536332D-9*temp*temp*temp*temp*temp 
